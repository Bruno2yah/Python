# conversão de tipos, coerção 
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos
# str, int, float, bool
print(1 + 1) # irá somar
print('a' + 'b') # irá concatenar as strings
# print('1' + 1) # da erro porque não da para concatenar string com number 
print(5 + 2, 'a' + 'b')
print('1', type('1'))
print(int('1'), type(int('1')))
print(int('1') + 1)
# int() classe que transforma em inteiro
# float() classe que transforma em float
# bool() classe que transforma em bool, por exemplo valor '' retorna False, valor 'aa' como tem valor retorna True 
# str() classe que transforma em string