adicao = 10 + 10
print('Adição:', adicao)

subtracao = 10 - 5
print('Subtração:', subtracao)

multiplicacao = 10 * 2
print('Multiplicação:', multiplicacao)

divisao = 10 / 2 # retorno sempre float
print('Divisão:', divisao)

divisao_inteira = 10 // 2 #se o divisor for inteiro, retorna valor int, se for divisor float, o valor será float, mas arredondado
print('Divisão inteira:', divisao_inteira)

exponenciacao = 2 ** 10 # Potenciação
print('Exponenciação:', exponenciacao)

modulo = 25 % 2 # resto
print('Módulo:', modulo)