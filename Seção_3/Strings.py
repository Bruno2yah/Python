"""
Python = linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro das aspas
"""
# aspas simples
print('Neymar Junior')
print('Neymar "Junior"')

# aspas duplas
print("Neymay Junior")
print("Neymay 'Junior'")

# escape
print("Neymar \"Junior\"") # "\" barra invertida é um caractere de escape, ele ignora o caractere que está depois dele

# r
print(r"Neymar \"Junior\"") # irá exibir o caractere de escape 